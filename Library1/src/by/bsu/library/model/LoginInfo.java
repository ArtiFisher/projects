package by.bsu.library.model;


public class LoginInfo {
    private String login;
    private int pass;

    public LoginInfo() {
    }

    public String getLogin() {
        return login;
    }

    public int getPass() {
        return pass;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public void setPass(int pass) {
        this.pass = pass;
    }
}
