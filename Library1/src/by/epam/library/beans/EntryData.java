package by.epam.library.beans;


public class EntryData {
    private String login;
    private String pass;

    public EntryData() {
    }

    public String getLogin() {
        return login;
    }

    public String getPass() {
        return pass;
    }

    public void setLogin(String login) {
        this.login = login;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }
}
